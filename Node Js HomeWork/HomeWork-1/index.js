// Fizz Buzz
function fizzBuzz(num){
    if(num % 3 == 0 && num % 5 == 0){
        console.log("FizzBuzz");
    }
    if(num % 3 == 0){
        console.log("Fizz");
    }
    if(num % 5 == 0){
        console.log("Buzz");
    }
}
fizzBuzz(4)
















